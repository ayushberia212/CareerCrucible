            <h4>THE COURSEWORK</h4>
            <div class="row">
              <div class="col-sm-12">
               
                 <p>The curriculum incudes deeper mathematics training and exposure to several science courses. It is essential to understand programming languages. Data structures, basic analogue and digital circuits, electronics devices, signal processing, microprocessors, control systems, communication systems, hydraulics and pneumatics, sensors, embedded systems, etc. specific to instrumentation and controls are courses in advanced process control, robotics, and automation, medical instrumentation, virtual instrumentation, neural networks, sensors, networking, etc.</p> </div>
