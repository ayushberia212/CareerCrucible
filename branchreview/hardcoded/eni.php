            <h4>ABOUT THE DISCIPLINE </h4>
            <div class="row">
              <div class="col-sm-12">
               
                <p>Instrumentation engineering is about managing processes by monitoring and measuring various parameters. Instrumentation engineering design, builds, install and maintain monitoring and measurement devices. Instrumentation ensures that problems are detected early and that processes run efficiently and smoothly. </p>
        <p> As a concept, Instrumentation per se is as old as human thought- human have always wanted to measure parameters in the world around. But department of Instrumentation engineering are of more recent vintage, emerging in the 1970s and 1980s. It originated as offshoots of electrical engineering. Then gradually, these programs became multi-disciplinary, seeking inputs from mechanical, electronics, computer science, chemical, etc. branches of engineering. Till the 1960s and 1970s Instrumentation was only about measurement so that humans could judge what part of the process to adjust from studying the values of parameters involved. Later, as electrical, electronics and computer science disciplines progressed; controlling processes also became an integral part of in Instrumentation.

        </p></div>
