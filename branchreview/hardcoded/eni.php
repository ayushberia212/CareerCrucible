        <div  class="  section-gray" style="margin-bottom:30px;">
          
            <h4>JOB OPPORTUNITIES </h4>
            <div class="row">
              <div class="col-sm-12">
               
                <p>Typically, graduates of tis discipline are exposed to programming and software design (for the purpose of processing instrumentation data and controlling the system) and are trained to think logically, which enables them to consider a career in software services. Normally, students also do several projects involving hardware. This training helps them to get job in several companies like device manufacturers. Alternatively, they can apply for jobs in companies which recruit electronics and communication engineers. Students are also trained in problem solving and troubleshooting. This enables them to easily switch careers and opt for non- core jobs.</p>
        <p> Almost every industry needs people with an instrumentation background. Industries where accidents could prove costly make it a point to recruit Instrumentation engineers. Hence job opportunities exist in chemical industries, petro-chemical (refineries), steel industry, etc. research labs like DRDO, and organizations like BARC, ISRO, etc. also need Instrumentation engineers.</p>
        <p> There are no large recruiters unlike IT sector and computer science and engineering and electrical engineering disciplines.</p> 
        <p> Higher education is a possibility. However, very few departments dedicated specifically to Instrumentation engineering exist in the top universities in India and overseas. Best is to identify electrical engineering departments with active research in instrumentation, and apply to those places. Start this research in your 4th year itself. Being an applied discipline, a lot of research takes place in the industry.
        </p> 
