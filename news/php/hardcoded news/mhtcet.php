            <p>
            1. MHT CET 2016 Registration:

In order to register online, candidates have to fill all the details below.
 For registration, information to be provided includes personal details
  and uploading of candidates’ recent colour passport photograph and signature.
   Uploaded photographs must be as per the specifications given below.

 
</p>
2. Filling of online MHT CET 2016 Application Form:

<p>Candidates have to enter their valid email id, mobile no. and a password at first login.
 Candidates must retain all details of these three particulars very carefully
  for downloading MHT CET 2016 Admit card and for all future correspondence with MHT CET Office.
   After entering these details in MHT CET 2016 Application Form,
    candidates can continue and complete their filling up of educational details.
</p>

<img src="img/mhreg.jpg" alt="" class="img-responsive">

<p>3. To Upload Photographs and Signature:

It is mandatory for all applicants to upload their scanned/digital recent colour photograph and signature in MHT CET 2016 Application Form. File size of each of the Photograph and Signature should not be more than 50 KB as the online application system will not allow uploading of files larger than 50 KB. All Photograph and Signature uploaded by the candidates must be in "jpeg" format only and file name of Photograph and Signature should preferably be photo.jpeg and sign.jpeg respectively.
</p>
 <p>

4. Uploading photograph:

Photograph must be snapped recently. Background of the Photograph must be light or white in colour. Face of the candidates must be clearly visible in the photograph as the applications with blur images are liable to be rejected.
</p>
 
<p>
5. Uploading Signature:

Candidates themselves should sign on a white paper with a black ink pen. Signature must be scanned in 200 dpi (dots per inch) scanner resolution settings and the file size should be less than 50 kb. After successful uploading of photograph and signature, the system will show on the screen “Photograph and Signature has been uploaded”. As soon as the images are correctly uploaded, candidates can now proceed further for payment of MHT CET 2016 Application Fee.
</p>
 
<p>

6. MHT CET 2016 Application Fee payment:

Downloading Bank Challan: Candidates have to download and print the MHT CET bank challan on an A4 size paper on a single page. MHT CET 2016 Registration No., Name of the Candidate, Address, Phone Number and Date of Registration will already be mentioned on the bank challan. Candidates have to pay the application fee for MHT CET 2016 through this challan at SBI branch.
</p><p>
Payment of fees: Candidates will be able to make the payment only after 48 hours of registration. Candidates belonging to open category are required to pay Rs 800.00 + Rs 60.00 (Bank Commission)and reserved category candidates are required to pay Rs 600.00 + Rs 60.00 (Bank Commission).On the printed challan, date of payment, Journal No/Bank Transaction ID, Branch Name & Code will be entered by the bank. After retaining the Bank’s copy of the bank challan rest of the bank challan will be returned to the candidate.
</p>
 <p>

7. Completion of online application Form: After 24 hours of payment of application fee applicants will be required to log in again for confirming whether the Bank transaction ID No. and date of payment have been updated. If it has been, applicants have to click on “Generate MHT CET 2016 Confirmation Page” and take print out of it which completes the application process for MHT CET 2016.
</p>
<p>Note: Candidates are advised to keep a copy of the Bank challan, MHT CET 2016 Registration Confirmation Pageprintout, MHT CET Registration No. and Password for all future correspondence with MHT CET Office.
</p>
