           <p>JEE aspirants, who have filled and submitted their JEE Main 2016 application form can check and rectify any discrepancy in their scanned uploaded images. 
            The official website of the exam has enabled ‘Remove Discrepancy in Uploaded Images’ feature, where applicants can check for their uploaded scanned images, whether or not the images are accepted.
            </p><p>Through this feature aspirants will be able to edit, replace and modify their images in case it is not as per the specified specifications provided by JEE Main 2016 office.
           </p>

