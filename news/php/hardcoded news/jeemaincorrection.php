          <div class="row" style="margin-top:20px;padding-left:40px;">
            <p>How to remove the discrepancies in uploaded images

For checking and repairing the discrepancies in the scanned uploaded images, candidates need to follow the below steps:<br>

1.Go to <a href="http://jeemain.nic.in/">http://jeemain.nic.in/</a><br>
2.Click on ‘Remove discrepancy in uploaded image(s)’ feature from the side panel<br>
3.After clicking on the link, candidates have to enter their Application Number and Password and hit the LOGIN button<br>
4.After entering the details, if no discrepancy is found in any of the uploaded images then following message will be displayed:<br>
<br>“No discrepancy found in your uploaded Photograph, Thumb Impression and Signature. You are not allowed to Login through this window.”

In case of any discrepancy i.e. it is not as per the prescribed specifications mentioned in the JEE Main 2016 Information brochure, then candidates have to upload the new images.

